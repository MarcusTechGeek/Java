package Chapter6.ex06_1_exercise;

public class Item { // Public class named 'Item'. This class can be accessed from other classes and packages.

        // Public member variables (fields) of the Item class:
        public int itemID;       // Item identifier (ID) as an integer. This can store a unique identifier for each item.
        public String desc;      // A string description of the item. This could hold a brief text about the item.
        public double price;     // The price of the item, represented as a double for decimal values.
        public int quantity;     // The quantity of the item available, represented as an integer.

}
