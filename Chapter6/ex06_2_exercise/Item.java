package Chapter6.ex06_2_exercise;

public class Item { // Public class named 'Item', which is accessible from other classes and packages.

        // Public member variables (fields) of the Item class:
        public int itemID;       // Integer field to store a unique identifier for each item.
        public String desc;      // String field to hold a description of the item.
        public double price;     // Double field to represent the price of the item, allowing for decimal values.
        public int quantity;     // Integer field to track the quantity of the item available.

}
